class loopsExample {

    public static void main(String args[]){

        //For Loop increment 1
        for(int i=0;i<=10;i++){
            System.out.print(i+ " ");
        }
            System.out.println();

        //for Loop increment 2
        for(int j=0;j<=10;j=j+2){
            System.out.print(j+" ");
        }
            System.out.println();

        //for Loop Decrement
        for(int k=10;k>=1;k--){
            System.out.print(k+" ");
        }
        System.out.println();

        //For decrement 2
        for(int p=10; p>=0; p=p-2){
            System.out.print(p+ " ");
        }

    }
}
