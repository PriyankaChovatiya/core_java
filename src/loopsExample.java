class loopsExample {

    public static void main(String args[]){

        //increment in while loop
        int i=1;
        while(i<=10){
            System.out.print(i+" ");
            i++;
        }
        System.out.println();

        //Decrement in while loop
        int j=10;
        while(j>=1){
            System.out.print(j+" ");
            j--;
        }

    }
}
